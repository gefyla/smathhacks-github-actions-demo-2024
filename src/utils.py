"""
Let's pretend this is very important source code that we don't want to break.

For instance, a library that other code utilizes and depends on.
"""

def addition(a, b):
    return a + b

def subtraction(a, b):
    return a - b

def equivalence(a, b):
    return a == b

def get_greater_value(a, b):
    return max(a, b)